class NewsCard {
    constructor(id, category, title, description, url, urlToImage, content) {
      this.id = id;
      this.category = category;
      this.title = title;
      this.description = description;
      this.url = url;
      this.urlToImage = urlToImage;
      this.content = content;
    }
  }
  
  export default NewsCard;